
public class GuitarSpec {

	Builder builder;
	String model;
	Type type;
	Wood backWood;
	Wood topWood;
	
	public Builder getBuilder() {
		return this.builder;
	}
	
	public String getModel() {
		return this.model;
	}
	
	public Type getType() {
		return this.type;
	}
	
	public Wood getBackWood() {
		return this.backWood;
	}
	
	public Wood getTopeWood() {
		return this.topWood;
	}
	
}
